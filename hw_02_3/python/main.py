# •Дано додатнє п’ятизначне число. Вивести число навпаки

num = int(input('Введіть число: '))
res = 0

while num > 0:
	buffer = num % 10
	num = math.floor(num / 10)
	res *= 10
	res = res + buffer

print(res)

