import math

# n учнів ділять k яблук порівну, остача - залишається в корзині.
# Скільки яблук дістанеться кожному учню? Скільки яблук залишається в корзині?

students = int(input('Введіть кількість студентів: '))
apples = int(input('Введіть кількість яблук: '))

remainder = apples % students
result = apples / students

print('Дано: \n'
      'Учнів:', students, '\n'
      'Яблук:', apples, '\n'
      'Залишок в корзині:', remainder, '\n'
      'Кожному учню дістанеться:', int(result), 'яблук')

