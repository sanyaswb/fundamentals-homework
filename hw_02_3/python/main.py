# Фірма для співробітників орендує двомісні офіси в бізнес-центрі. Відома кількість
# співробітників у кожному з трьох підрозділів. Вивести найменшу кількість кабінетів, яку потрібно орендувати для них.

subdivisionOne = int(input('Введіть число працівників першого підрозділу: '))
subdivisionTwo = int(input('Введіть число працівників другого підрозділу: '))
subdivisionThree = int(input('Введіть число працівників третього підрозділу: '))

print('Число працівників першого підрозділу:', subdivisionOne, '\n'
      'Число працівників другого підрозділу:', subdivisionTwo, '\n'
      'Число працівників третього підрозділу:', subdivisionThree, '\n')

sum = +subdivisionOne + +subdivisionTwo + +subdivisionThree

print('Сума працівників всіх підрозділів: ', sum, '\n')

sum = math.ceil(sum / 2)

